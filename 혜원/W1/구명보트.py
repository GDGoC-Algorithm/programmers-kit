# 적용 알고리즘의 개념
    # Greedy 알고리즘:
    # 이 문제에서는 가장 무거운 사람을 먼저 태우되, 같이 탈 수 있다면 가장 가벼운 사람을 함께 태우는 선택이 유리함

# 문제 풀이를 위한 접근 방식(or 개념) 설명
    # 문제 목표: 모든 사람을 구출하기 위해 필요한 구명보트 개수의 최솟값을 구해야 함 (min)
    # 상태 분류:
        # 가장 무거운 사람
        # 가장 가벼운 사람
    # 제한 조건:
        # 보트에는 최대 2명만 탈 수 있음
        # 두 사람의 몸무게 합이 limit 이하여야 함
    # 문제에서 얻을 수 있는 힌트:
        # 한 보트에 2명까지 탈 수 있으므로, 혼자 타는 경우를 최대한 줄여야 함
        # 가장 무거운 사람은 반드시 먼저 고려해야 함
        # 가장 무거운 사람이 누구와도 못 타면 혼자 타야 함
        # 가장 무거운 사람과 같이 탈 수 있는 후보 중에서는 가장 가벼운 사람을 태우는 것이 유리함
        # 정렬 후 양쪽 끝에서 비교하는 방식이 자연스러움

# 기본 코드와 개선한 코드 비교
        # 처음에는 사람 한 명을 기준으로 같이 탈 수 있는 사람을 생각함
        # 개선 코드는 몸무게를 정렬한 뒤, 가장 가벼운 사람(left)과 가장 무거운 사람(right)을 같이 태울 수 있는지만 확인
        # 같이 탈 수 있으면 둘 다 태우고, 못 타면 가장 무거운 사람만 먼저 태우기 
        # O(n log n)

# 사용 라이브러리 정리
    # 인덱스(left, right)
        # 리스트 양 끝 원소를 가리킴
        # 문제에서: 가장 가벼운 사람과 가장 무거운 사람을 비교하기 위해 사용

def solution(people, limit):
    people.sort()  

    left = 0  # 가장 가벼운 사람의 위치
    right = len(people) - 1  # 가장 무거운 사람의 위치
    boat = 0  # 사용한 구명보트 개수

    while left <= right:  # 아직 태우지 않은 사람이 남아 있으면 계속 진행
        if people[left] + people[right] <= limit:  # 가장 가벼운 사람과 가장 무거운 사람이 함께 탈 수 있으면
            left += 1  # 가장 가벼운 사람도 같이 태웠으므로 다음 사람으로 이동

        right -= 1  # 가장 무거운 사람은 이번 보트에 반드시 탔으므로 한 칸 이동
        boat += 1  # 보트 한 대 사용

    return boat  
