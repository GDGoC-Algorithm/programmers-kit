# 적용 알고리즘의 개념
    # 문자열 처리 + 정렬 + 집합(set)
    # 문자열을 집합 단위로 나눈 뒤,
    # 원소 개수가 적은 집합부터 확인하면서 새로 나오는 숫자를 순서대로 넣는 방식

# 문제 풀이를 위한 접근 방식(개념) 설명
    # 이 문제는 문자열로 주어진 집합들을 이용해서 원래 튜플 순서를 복원하면 된다.

    # 예를 들어
    # {{2},{2,1},{2,1,3},{2,1,3,4}}
    # 이런 형태라면 원소 개수가 1개인 집합에는 첫 번째 원소만 들어 있고,
    # 원소 개수가 2개인 집합에는 앞의 두 원소가 들어 있다.

    # 따라서
    # 1. 문자열에서 각 집합을 꺼내고
    # 2. 집합 길이 기준으로 정렬한 뒤
    # 3. 앞에서부터 보면서 아직 answer에 없는 숫자를 하나씩 추가하면 된다.

# 기본 코드와 개선한 코드 비교
    # 집합 길이 순서대로 정렬해 해결할 수 있다. 
    # set을 사용해 이미 나온 숫자인지 빠르게 확인할 수 있다.
    # O(n log n)

# 사용 라이브러리 정리
    # 별도 라이브러리 없음
    # set 사용

def solution(s):
    # 양쪽의 "{{", "}}"를 제거하고 집합 단위로 나누기
    arr = s[2:-2].split("},{")

    data = []

    # 문자열 숫자들을 정수 리스트로 바꾸기
    for x in arr:
        nums = list(map(int, x.split(",")))
        data.append(nums)

    # 원소 개수가 적은 집합부터 정렬
    data.sort(key=len)

    answer = []
    chk = set()

    # 앞에서부터 새로 나오는 숫자를 answer에 추가
    for nums in data:
        for num in nums:
            if num not in chk:
                answer.append(num)
                chk.add(num)

    return answer