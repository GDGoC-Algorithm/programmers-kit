# 적용 알고리즘의 개념
    # Greedy 알고리즘:
    # 이 문제에서는 현재 숫자보다 앞의 숫자가 더 작으면, 그 작은 숫자를 제거하는 선택이 유리함

# 문제 풀이를 위한 접근 방식(or 개념) 설명
    # 문제 목표: k개의 숫자를 제거해서 만들 수 있는 가장 큰 숫자 구하기 (max)
    # 입력 형태:
        # number는 문자열 형태의 숫자
        # k는 제거할 숫자의 개수
    # 제한 조건:
        # number의 길이가 최대 1,000,000자리이므로 모든 경우의 수를 비교하는 방식은 불가능
        # 숫자를 제거해도 남은 숫자의 상대적인 순서는 유지됨
    # 문제에서 얻을 수 있는 힌트:
        # **앞자리가 클수록 전체 숫자가 커짐
        # **뒤에 더 큰 숫자가 나오면, 앞의 작은 숫자는 제거하는 것이 유리함
        # 한 번 훑으면서 처리할 수 있는 방식이 필요함

# 사용 라이브러리 정리
    # list - 스택처럼 사용
        # 뒤에 원소를 넣고 빼기 쉬움
        # 문제에서: 현재까지 선택한 숫자를 저장하기 위해 사용
    # append() - 원소 추가
        # 스택의 맨 뒤에 숫자를 추가
    # pop() - 원소 제거
        # 스택의 맨 뒤 숫자를 제거
        # 문제에서: 현재 숫자보다 작은 앞 숫자를 제거할 때 사용
    # join() - 문자열 합치기
        # 리스트에 저장된 숫자 문자들을 최종 문자열로 합침


def solution(number, k):
    stack = []  # 선택한 숫자를 저장할 스택

    for num in number:  
        # 현재 숫자가 더 크고, 아직 제거할 수 있고, 비교할 값이 스택에 있을 때
        while k > 0 and stack and stack[-1] < num:
            stack.pop()      # 앞에 있는 더 작은 숫자를 제거 (더 큰 수를 만들기 위해)
            k -= 1           # 제거 횟수 1 감소

        stack.append(num)
      
    # 모든 숫자를 다 확인했는데도 제거 횟수가 남아 있다면
    if k > 0:
        stack = stack[:-k]  # 뒤에서 k개를 제거
      
    return ''.join(stack)  
